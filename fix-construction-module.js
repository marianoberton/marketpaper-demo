const { createClient } = require('@supabase/supabase-js');
const fs = require('fs');

// Leer variables de entorno
const envContent = fs.readFileSync('.env', 'utf8');
const envLines = envContent.split('\n');
envLines.forEach(line => {
  const trimmed = line.trim();
  if (trimmed && !trimmed.startsWith('#')) {
    const [key, ...parts] = trimmed.split('=');
    if (key && parts.length > 0) {
      process.env[key] = parts.join('=').replace(/^[\"']|[\"']$/g, '');
    }
  }
});

const supabase = createClient(
  process.env.NEXT_PUBLIC_SUPABASE_URL,
  process.env.SUPABASE_SERVICE_ROLE_KEY
);

async function fixConstructionModule() {
  console.log('üîß Corrigiendo m√≥dulo construcci√≥n...');
  
  // 1. Obtener company_id de Inted
  const { data: company } = await supabase
    .from('companies')
    .select('id, name')
    .eq('name', 'Inted')
    .single();
    
  if (!company) {
    console.log('‚ùå No se encontr√≥ la empresa Inted');
    return;
  }
  
  console.log('‚úÖ Empresa encontrada:', company);
  
  // 2. Buscar el m√≥dulo "Construccion" (con may√∫scula)
  const { data: module } = await supabase
    .from('modules')
    .select('id, name')
    .eq('name', 'Construccion')
    .single();
    
  if (!module) {
    console.log('‚ùå No se encontr√≥ el m√≥dulo Construccion');
    return;
  }
  
  console.log('‚úÖ M√≥dulo Construccion encontrado:', module);
  
  // 3. Verificar si ya existe la relaci√≥n company_modules
  const { data: existingRelation } = await supabase
    .from('company_modules')
    .select('*')
    .eq('company_id', company.id)
    .eq('module_id', module.id)
    .single();
    
  if (existingRelation) {
    console.log('üîÑ Relaci√≥n ya existe:', existingRelation);
    
    if (!existingRelation.is_active) {
      console.log('üîÑ Activando m√≥dulo...');
      
      const { error: updateError } = await supabase
        .from('company_modules')
        .update({ is_active: true })
        .eq('company_id', company.id)
        .eq('module_id', module.id);
        
      if (updateError) {
        console.log('‚ùå Error actualizando relaci√≥n:', updateError.message);
        return;
      }
      
      console.log('‚úÖ M√≥dulo construcci√≥n ACTIVADO para Inted');
    } else {
      console.log('‚úÖ M√≥dulo construcci√≥n ya est√° ACTIVO para Inted');
    }
  } else {
    console.log('‚ûï Creando nueva relaci√≥n company_modules...');
    
    const { error: insertError } = await supabase
      .from('company_modules')
      .insert({
        company_id: company.id,
        module_id: module.id,
        is_active: true
      });
      
    if (insertError) {
      console.log('‚ùå Error creando relaci√≥n:', insertError.message);
      return;
    }
    
    console.log('‚úÖ M√≥dulo construcci√≥n ACTIVADO para Inted');
  }
  
  // 4. Verificar que se activ√≥ correctamente usando la vista
  const { data: verification } = await supabase
    .from('company_modules')
    .select(`
      is_active,
      modules!inner(name)
    `)
    .eq('company_id', company.id)
    .eq('is_active', true);
    
  console.log('\nüîß M√≥dulos activos para Inted despu√©s de la activaci√≥n:');
  verification?.forEach(cm => console.log(`  - ${cm.modules.name}`));
  
  const hasConstruction = verification?.some(cm => 
    cm.modules.name === 'Construccion' || cm.modules.name === 'construccion'
  );
  
  console.log(`\n${hasConstruction ? '‚úÖ' : '‚ùå'} M√≥dulo construcci√≥n ${hasConstruction ? 'ACTIVO' : 'NO ACTIVO'} para Inted`);
  
  if (hasConstruction) {
    const correctUrl = `http://localhost:3000/workspace/construccion?company_id=${company.id}`;
    console.log('\nüöÄ Ahora puedes acceder al m√≥dulo construcci√≥n en:');
    console.log(correctUrl);
    
    console.log('\nüìù Nota: El m√≥dulo se llama "Construccion" en la BD pero la URL usa "construccion" (min√∫scula)');
  }
}

fixConstructionModule().catch(console.error);